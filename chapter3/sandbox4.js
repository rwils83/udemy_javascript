// switch statements


const grade = 'E';


switch(grade){
	case 'A':
		console.log('Smarty pants');
		break;
	case 'B':
		console.log('Good Job');
		break;
	case 'C':
		console.log('You could have done better');
		break;
	case 'D':
		console.log('You suck');
		break;
	case 'F':
		console.log('Dumbass');
		break;
	default: 
		console.log('You so dumb you couldn\'t event put in a real grade');
		break;


}

/// switch statements use strict comparison, type matters '50'!=50
